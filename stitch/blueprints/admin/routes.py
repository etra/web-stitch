@bp.route('/')
@login_required
@admin_required
def dashboard():
    """Admin dashboard with platform metrics"""
    total_users = db.session.query(func.count(User.id)).scalar()
    total_projects = db.session.query(func.count(Project.id)).filter(
        Project.status != ProjectStatus.DELETED
    ).scalar()

    return render_template('admin/dashboard.html',
                           total_users=total_users,
                           total_projects=total_projects)


@bp.route('/users')
@login_required
@admin_required
def users():
    """List all users"""
    all_users = db.session.query(
        User,
        func.count(Project.id).label('project_count')
    ).outerjoin(
        Project,
        (User.id == Project.user_id) & (Project.status != ProjectStatus.DELETED)
    ).group_by(User.id).order_by(User.created_at.desc()).all()

    return render_template('admin/users.html', users=all_users)


@bp.route('/users/<user_id>')
@login_required
@admin_required
def user_detail(user_id):
    """Admin user detail page with their projects"""
    user = User.query.get(user_id)
    if not user:
        flash('User not found.', 'warning')
        return redirect(url_for('admin.users'))

    user_projects = Project.query.filter(
        Project.user_id == user_id,
        Project.status != ProjectStatus.DELETED,
    ).order_by(Project.created_at.desc()).all()

    return render_template('admin/user_detail.html',
                           user=user,
                           projects=user_projects,
                           ProjectStatus=ProjectStatus)


